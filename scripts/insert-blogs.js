// Function to insert blog posts
async function insertBlogPost(post) {
  const url = 'https://safestorage.in/back/app/insert_blog_content';
  
  try {
    const response = await fetch(url, {
      method: 'POST',
      headers: {
        'Content-Type': 'application/x-www-form-urlencoded',
      },
      body: new URLSearchParams(post)
    });
    
    const result = await response.text();
    console.log(`Inserted: ${post.slug} - ${result}`);
    return { slug: post.slug, status: result };
  } catch (error) {
    console.error(`Error inserting ${post.slug}:`, error);
    return { slug: post.slug, status: 'error', error: error.message };
  }
}

// Insert all blog posts with delay to avoid overwhelming the server
async function insertAllPosts() {
  console.log(`Starting to insert ${allBlogPosts.length} blog posts...`);
  
  const results = [];
  for (let i = 0; i < allBlogPosts.length; i++) {
    const post = allBlogPosts[i];
    console.log(`Inserting ${i + 1}/${allBlogPosts.length}: ${post.slug}`);
    
    const result = await insertBlogPost(post);
    results.push(result);
    
    // Add a small delay between requests
    await new Promise(resolve => setTimeout(resolve, 500));
  }
  
  // Summary
  console.log('\n=== INSERTION SUMMARY ===');
  console.log(`Total posts: ${results.length}`);
  console.log(`Successful: ${results.filter(r => r.status === 'success').length}`);
  console.log(`Failed: ${results.filter(r => r.status !== 'success').length}`);
  
  const failed = results.filter(r => r.status !== 'success');
  if (failed.length > 0) {
    console.log('\nFailed posts:');
    failed.forEach(f => console.log(`- ${f.slug}: ${f.status}`));
  }
}

